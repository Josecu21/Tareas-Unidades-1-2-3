'''Calcular el área y el perímetro de un Rectángulo

En el siguiente ejercicio se solicita calcular el área y el perímetro de un Rectángulo, para ello deberemos crear las siguientes variables:

alto (int)

ancho (int)'''


a=int(input('Ingrese el alto del rectangulo: '))
b=int(input('Ingrese el ancho del rectangulo: '))
perimetro=((2*a)+(2*b))
print('El perimtero del rectangulo es ',perimetro)
area = (a*b)
print('El área del rectangulo es ',area)
